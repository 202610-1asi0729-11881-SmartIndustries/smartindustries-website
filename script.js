let currentLang = 'es';

function updateTexts() {
    // Actualizar texto normal
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (langData[currentLang][key]) el.innerHTML = langData[currentLang][key];
    });
    // Actualizar placeholders del formulario
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (langData[currentLang][key]) el.placeholder = langData[currentLang][key];
    });
}

document.getElementById('lang-btn').addEventListener('click', () => {
    currentLang = currentLang === 'es' ? 'en' : 'es';
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (langData[currentLang][key]) el.innerHTML = langData[currentLang][key];
    });
});

const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            entry.target.classList.add('reveal');
        }
    });
}, { threshold: 0.15 });

const updateLanguage = () => {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (langData[currentLang][key]) el.innerHTML = langData[currentLang][key];
    });
    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (langData[currentLang][key]) el.placeholder = langData[currentLang][key];
    });
};

document.getElementById('lang-btn-float').addEventListener('click', () => {
    currentLang = currentLang === 'es' ? 'en' : 'es';
    updateLanguage();
});

window.addEventListener('DOMContentLoaded', () => {
    document.querySelector('.hero-content').classList.add('reveal');
    document.querySelectorAll('.feat-card, .step-item, .case-card, .price-card, .contact-container').forEach(el => observer.observe(el));
});

document.querySelectorAll('.nav-links a, .footer-links a').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        const href = this.getAttribute('href');
        if (href.startsWith("#")) {
            e.preventDefault();
            const targetId = href.substring(1);
            const targetElement = document.getElementById(targetId);
            if (targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 70, // Offset por la navbar
                    behavior: 'smooth'
                });
            }
        }
    });
});

